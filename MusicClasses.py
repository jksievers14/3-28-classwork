# use classes to manage multiple data attributes

class Music():
    # this sets up the variables in the object
    def __init_(self):
        self.platform=''
        self.artist=''
        self.genre=''
        self.age=''
        self.sex=''
        self.albums=''

# create an instance of this class
ipod = Music()

ipod.platform = 'iTunes'
ipod.genre = 'Hip Hop'
ipod.artist = 'Travis Scott'
ipod.age='24'
ipod.sex='Male'
ipod.albums= '2'
